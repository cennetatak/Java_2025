    public static void main(String[] args) {

        // Kullanicidan aldigi urun adedini ve indirimsiz birim fiyatini alin,
        // kullaniciya musteri karti olup olmadigini sorun.
        // Musteri karti varsa 10 urunden fazla alirsa %20,
        //                        az alirsa %15 indirim yapin,
        // Musteri karti yoksa 10 urunden fazla alirsa %15,
        //                        az alirsa %10 indirim yapin

        Scanner scanner = new Scanner(System.in);
        System.out.println("aldığınız ürün adedini giriniz...");
        int adet = scanner.nextInt();

        System.out.println("Aldığınız ürünün birim fiyatını giriniz...");
        double birimFiyati = scanner.nextDouble();

        System.out.println("müşteri kartınız var mı ?   'E':  evet   'H':  hayır");
        char kartVarMi= scanner.next().toUpperCase().charAt(0);
        double indirimsizFiyatToplami= adet*birimFiyati;

        //ana değişken : alınan ürün adedi

        if (adet>=10){ // 10adet ve üzeri ürün alanlar
           if (kartVarMi== 'E')
               System.out.println("%20 indirimli fiyat:  " +indirimsizFiyatToplami*80/100 );
           else if (kartVarMi=='H') {
               System.out.println("%15 indirimli fiyat:  " + indirimsizFiyatToplami*85/100);
           }


        } else if (adet<10) {// 10 adetten az ürün alanlar
            if (kartVarMi=='E'){
                System.out.println("%15 indirimli fiyat: " + indirimsizFiyatToplami*85/100);
            } else if (kartVarMi=='H') {
                System.out.println("%10 indirimli fiyat:  " + indirimsizFiyatToplami*90/100);
            }


        }else System.out.println("Ürün adedi sorusuna verilen cevap hatalı");




































































































































































































































































































































































































































    }
